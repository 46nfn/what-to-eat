/**
 * AI 推荐引擎
 * 根据用户上下文匹配最佳食物
 */
export const recommendFoods = ({
  mealType, budget, tastePrefs = [], allergies = [],
  scene, cuisine, history = [], keywords = '', count = 3,
}) => {
  let list = [...FOOD_DB]

  // 餐段过滤
  if (mealType) list = list.filter(f => f.m.includes(mealType))
  // 预算过滤
  if (budget) {
    const [min, max] = budget
    list = list.filter(f => f.p[0] <= max && f.p[1] >= min)
  }
  // 场景过滤
  if (scene) list = list.filter(f => f.s.includes(scene))
  // 菜系过滤
  if (cuisine) list = list.filter(f => f.c === cuisine || f.c === cuisine.replace('菜',''))
  // 忌口过滤
  if (allergies.length) {
    list = list.filter(f => !allergies.some(a => (f.n + f.t.join(' ')).includes(a)))
  }
  // 关键词
  if (keywords) {
    const kw = keywords.toLowerCase()
    list = list.filter(f => (f.n + f.c + f.t.join(' ') + f.d).toLowerCase().includes(kw))
  }

  // 评分排序
  list = list.map(f => {
    let score = Math.random() * 2
    tastePrefs.forEach(t => { if (f.t.includes(t)) score += 3 })
    const histCuisines = history.filter(h => h.rating >= 4).map(h => h.cuisine)
    if (histCuisines.includes(f.c)) score += 1
    if (history.some(h => h.name === f.n)) score -= 0.5
    return { ...f, score }
  })
  list.sort((a, b) => b.score - a.score)

  // 去重取 Top N
  const seen = new Set()
  return list.filter(f => { const k = f.n; if (seen.has(k)) return false; seen.add(k); return true }).slice(0, count)
}

